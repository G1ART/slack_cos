/**
 * `start_project` 대표 표면 계약 — Council/승인/APR 없이 정렬만.
 * @param {string} goalText 인입 본문(툴 목표)
 * @returns {string[]}
 */
export function buildStartProjectSurfaceBodyLines(goalText) {
  const g = String(goalText || '').trim();
  const cal = /캘린더|스케줄|달력|일정|예약|멤버|갤러리|아뜰|공간|대관|룸/u.test(g);

  /** @type {string[]} */
  const assumptions = cal
    ? [
        '웹 MVP·반응형 우선; **무응답 시** 모바일은 *조회 위주*·편집은 웹/관리자 중심으로 가정합니다.',
        '기본 뷰는 **월+주** 중심(일 뷰는 후순위).',
        '**공간(대관/룸) 예약**과 **멤버 개인 일정**은 데이터 모델은 분리하고, 한 앱에서 탭/필터로 같이 볼 수 있다고 가정합니다.',
        '**반복 일정**은 v1에서 제외(수동 복제).',
        '**승인**이 필요한 유형은 *전시·대관·외부 대관* 정도만 관리자 승인, 나머지는 멤버 직접 등록으로 가정합니다.',
      ]
    : [
        '웹 MVP·반응형 우선; 필요 시 이후 네이티브·오프라인 고려.',
        '첫 사용자층은 **내부 멤버** 기준.',
        '외부 SaaS·결제·SSO 연동은 **명시 전까지** v1 범위 밖.',
        '역할은 **관리자 + 일반 멤버** 2단계로 가정합니다.',
      ];

  /** @type {string[]} */
  const questions = cal
    ? [
        '1. 모바일에서 **편집**까지 필요한가요, 아니면 **조회 위주**면 될까요? *(무응답: 조회 위주)*',
        '2. 첫 화면 **기본 뷰**는 월 / 주 / 일 중 무엇을 기본으로 할까요? *(무응답: 월+주)*',
        '3. 공간 예약과 개인 일정을 **한 화면·필터**로 볼지, **UI도 완전 분리**할까요? *(무응답: 데이터 분리 + 단일 앱에서 필터)*',
      ]
    : [
        '1. 주 사용 환경: **웹만**인가요, **모바일 네이티브**까지 v1에 넣을까요? *(무응답: 웹 우선)*',
        '2. 첫 배포에서 *반드시* 되어야 하는 동작을 **한 가지**만 짚어 주세요. *(무응답: 위 MVP 가정 유지)*',
        '3. 외부 툴(구글 캘린더 등) **연동이 v1 필수**인가요? *(무응답: 불필요)*',
      ];

  return [
    '*[정렬 · 프로젝트/툴 시작]*',
    '',
    `*이해한 바:* 새 도구·구현 킥오프로, _${g.slice(0, 520)}${g.length > 520 ? '…' : ''}_`,
    '_위가 다르면 한 줄만 정정해 주세요._',
    '',
    '*기본 가정 (무응답 시 그대로 진행)*',
    ...assumptions.map((s) => `- ${s}`),
    '',
    '*확인 질문* — 아래만 답해 주시면 됩니다. (전략·PM 인터뷰 전체는 `협의모드:`)',
    ...questions,
    '',
    '*다음 산출물* — 채널/DM에서 보내면 동일 문장이 **실행 큐(spec)** 로 적재됩니다.',
    '`실행큐계획화 최근` 또는 `실행큐계획화 CWS-…` 한 줄로 **PLN·WRK**까지 승격한 뒤 `커서발행` 등 코딩 루프로 넘기면 됩니다.',
    '_이 턴에는 **승인 대기열(APR)을 만들지 않습니다.** 범위가 패킷/옵션 객체로 고정된 뒤에만 승인 게이트를 둡니다._',
  ];
}
